export function BottomMenuBar({ onCreatePress, createOpen }: BottomMenuBarProps) {
  const { theme } = useTheme();
  const isDark = theme === "dark";
  const { pathname } = useLocation();
  const navigate = useNavigate();
  const activeTab = getActiveTab(pathname);

  return (
    <div
      className="fixed bottom-0 left-0 right-0"
      style={{ background: isDark ? "#0f0f0f" : "#ffffff", zIndex: createOpen ? 210 : 50 }}
    >
      <div
        className="h-[0.5px] w-full"
        style={{ background: isDark ? "rgba(255,255,255,0.1)" : "rgba(0,0,0,0.1)" }}
      />
      <div className="flex items-center justify-around h-14">
        {TABS.map((tab) => {
          const isActive = tab.id === activeTab;
          const Icon = tab.icon;
          return (
            <button
              key={tab.id}
              className="flex items-center justify-center w-[74px] h-14 cursor-pointer bg-transparent border-none"
              style={{
                color: isDark
                  ? isActive ? "#ffffff" : "rgba(255,255,255,0.4)"
                  : isActive ? "#000000" : "rgba(0,0,0,0.4)",
              }}
              onClick={() => {
                if (tab.id === "create") {
                  onCreatePress();
                } else if (tab.path) {
                  navigate(tab.path);
                }
              }}
            >
              {tab.id === "create" && createOpen ? <XIcon /> : <Icon />}
            </button>
          );
        })}
      </div>
    </div>
  );
}
